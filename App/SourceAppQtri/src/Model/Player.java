
package Model;


public class Player {
    private String Name;
    private String UserName;
    private String PassWord;
    private String Email;

    public Player() {
    }

    public Player(String Name, String UserName, String PassWord, String Email) {
        this.Name = Name;
        this.UserName = UserName;
        this.PassWord = PassWord;
        this.Email = Email;
    }
    
    
}

